import type { CollectionConfig } from 'payload'
import { isAdmin, isAuthed, isEditorOrAdmin } from '../access/byRole'
import { checkIfUnmodifiedSince } from '../hooks/checkIfUnmodifiedSince'
import { segmentChinese } from '../hooks/segmentChinese'
import { setLastEditedBy } from '../hooks/setLastEditedBy'
import { updateTranscriptionSearchVector } from '../hooks/updateSearchVector'

export const Transcriptions: CollectionConfig = {
  slug: 'transcriptions',
  admin: {
    useAsTitle: 'document',
    defaultColumns: ['document', 'transcriber', 'updatedAt'],
    listSearchableFields: ['text'],
  },
  access: {
    read: isAuthed,
    create: isEditorOrAdmin,
    update: isEditorOrAdmin,
    delete: isAdmin,
  },
  versions: {
    maxPerDoc: 20,
  },
  hooks: {
    beforeChange: [
      checkIfUnmodifiedSince,
      setLastEditedBy,
      ({ data }) => {
        // Re-segment on every save so textSegmented stays in sync with text.
        // Keeps Postgres tsvector indexing consistent.
        data.textSegmented = segmentChinese(data.text)
        return data
      },
    ],
    afterChange: [updateTranscriptionSearchVector],
  },
  fields: [
    {
      name: 'document',
      type: 'relationship',
      relationTo: 'documents',
      required: true,
      hasMany: false,
      admin: {
        description: 'Each Document has at most one Transcription.',
      },
    },
    {
      name: 'text',
      type: 'textarea',
      label: '中文录入',
    },
    {
      name: 'textSegmented',
      type: 'textarea',
      admin: {
        hidden: true,
        readOnly: true,
        description: 'Auto-generated by nodejieba for search.',
      },
    },
    {
      name: 'transcriber',
      type: 'relationship',
      relationTo: 'users',
      hasMany: false,
    },
    {
      name: 'lastEditedBy',
      type: 'relationship',
      relationTo: 'users',
      hasMany: false,
      admin: {
        readOnly: true,
        description: 'Auto-set on every save.',
      },
    },
  ],
}
